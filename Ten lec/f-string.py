# f-Strings in Python

name = "Bilal"

print(f"My name is {name}")

# Multiple variables

age = 25

print(f"My name is {name} and I am {age} years old")

# String formatting

print(f"My name is {name.capitalize()}, and I am {age} years old")

from IPython.display import display

name = "Bilal Hussain"
display(name)

name = "          Bilal Hussain         "
display(name)
